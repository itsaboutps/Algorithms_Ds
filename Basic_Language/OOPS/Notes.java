/**
 No, that statement is not correct in Java. 
 In Java, a class can both extend another class and implement one or more interfaces simultaneously.
 When a class extends another class, it inherits all the non-private fields and methods of the superclass.
 This is known as class inheritance.
 When a class implements an interface, it declares that it provides the behavior specified by that interface.
 The class must then provide implementations for all the methods defined in the interface. This is known as interface implementation.
 */
